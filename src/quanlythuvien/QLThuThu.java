    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        String matk = txtMaTT.getText();
        String hoten = txtHoTenTT.getText();
        String email = txtEmail.getText();
        String matkhau = txtMatKhauTT.getText();
        String vaitro = cbxVaitro.getSelectedItem().toString();
        String trangthai = cbxTrangthai.getSelectedItem().toString();
        int vaitromoi;
        int trangthaimoi;
        if ("Thủ thư".equals(vaitro)) {
            vaitromoi = 0;
        } else {
            vaitromoi = 1;
        }
        if ("Nghỉ làm".equals(trangthai)) {
            trangthaimoi = 0;
        } else {
            trangthaimoi = 1;
        }

        boolean check = thuthudao.edit(matk, hoten, email, matkhau, vaitromoi, trangthaimoi);
        if (check) {
            JOptionPane.showMessageDialog(this, "Cập nhật thành công!");
        } else {
            JOptionPane.showMessageDialog(this, "Lỗi khi cập nhật!");
        }
        list = thuthudao.getList();
        filltotable();
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        String manv = txtMaTT.getText();
        String hoten = txtHoTenTT.getText();
        String matkhau = txtMatKhauTT.getText();
        String email = txtEmail.getText();
        int vaitro = cbxVaitro.getSelectedItem().equals("Thủ thư") ? 0 : 1;
        int trangthai = cbxTrangthai.getSelectedItem().equals("Nghỉ làm") ? 0 : 1;

        boolean check = thuthudao.add(manv, hoten, matkhau, email, vaitro, trangthai);
        if (check) {
            JOptionPane.showMessageDialog(this, "Saved");
        } else {
            JOptionPane.showMessageDialog(this, "Error!");
        }
        list = thuthudao.getList();
        filltotable();
    }//GEN-LAST:event_btnThemActionPerformed

