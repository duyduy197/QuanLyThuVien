    public void filltotable() {
        tblModel = (DefaultTableModel) tblthuthu.getModel();
        tblModel.setRowCount(0);
        String vaiTro = "";
        String trangThai = "";
        for (model.ThuThu nv : list) {
            if (nv.getVaitro() == 1) {
                vaiTro = "Admin";
            } else {
                vaiTro = "Thủ thư";
            }
            if (nv.getTrangthai() == 1) {
                trangThai = "Đang làm";
            } else {
                trangThai = "Nghỉ làm";
            }
            Object[] row = new Object[]{nv.getMaTK(), nv.getTenTK(), nv.getMatkhau(), nv.getEmail(), vaiTro, trangThai};
            tblModel.addRow(row);
        }
    }

    public void showdetail(int index) {
        try {
            txtMaTT.setText(tblthuthu.getValueAt(index, 0) + "");
            txtHoTenTT.setText(tblthuthu.getValueAt(index, 1) + "");
            txtMatKhauTT.setText(tblthuthu.getValueAt(index, 2) + "");
            txtEmail.setText(tblthuthu.getValueAt(index, 3) + "");
            Object objVaitro = tblthuthu.getValueAt(index, 4);
            Object objTrangThai = tblthuthu.getValueAt(index, 5);
            if (objVaitro instanceof Integer) {
                int vaitro = (int) objVaitro;
                if (vaitro == 0) {
                    cbxVaitro.setSelectedItem("Thủ thư");
                } else {
                    cbxVaitro.setSelectedItem("Admin");
                }
            } else if (objVaitro instanceof String) {
                String vaitro = (String) objVaitro;
                cbxVaitro.setSelectedItem(vaitro);
            }

            if (objTrangThai instanceof Integer) {
                int trangthai = (int) objTrangThai;
                if (trangthai == 0) {
                    cbxTrangthai.setSelectedItem("Nghỉ làm");
                } else {
                    cbxTrangthai.setSelectedItem("Đang làm");
                }
            } else if (objTrangThai instanceof String) {
                String trangthai = (String) objTrangThai;
                cbxTrangthai.setSelectedItem(trangthai);
            }

        } catch (Exception ex) {
            System.out.println(ex);
        }
    }

