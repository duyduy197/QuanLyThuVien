    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        String maloai = txtMaloai.getText();
        String tenloai = txtTenloai.getText();
        int trangthai = cbotrangThai.getSelectedItem().equals("Nghỉ làm") ? 0 : 1;

        boolean check = loaisachdao.add(maloai, tenloai, trangthai);
        if (check) {
            JOptionPane.showMessageDialog(this, "Saved");
        } else {
            JOptionPane.showMessageDialog(this, "Error!");
        }
        list = loaisachdao.getList();
        fillToTable();
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        String maLoai = txtMaloai.getText();
        boolean xoa = loaisachdao.delete(maLoai);
        if (xoa) {
            JOptionPane.showMessageDialog(this, "Deleted !");
        } else {
            JOptionPane.showMessageDialog(this, "Error !");
        }
        list = loaisachdao.getList();
        fillToTable();
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        String maloai = txtMaloai.getText();
        String tenloai = txtTenloai.getText();
        String trangThai = cbotrangThai.getSelectedItem().toString();
        int trangthaimoi;
        if ("Còn".equals(trangThai)) {
            trangthaimoi = 0;
        } else {
            trangthaimoi = 1;
        }
        boolean check = loaisachdao.edit(maloai, tenloai, trangthaimoi);
        if (check) {
            JOptionPane.showMessageDialog(this, "Cập nhật thành công!");
        } else {
            JOptionPane.showMessageDialog(this, "Lỗi khi cập nhật!");
        }
        list = loaisachdao.getList();
        fillToTable();
    }//GEN-LAST:event_btnSuaActionPerformed

