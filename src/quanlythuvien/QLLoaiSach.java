    public void comboboxtrangthai() {
        cbotrangThai.addItem("Còn");
        cbotrangThai.addItem("Hết");
        System.out.println("Items in combo box" + cbotrangThai.getItemCount());
    }

    public void fillToTable() {
        tblModel = (DefaultTableModel) tblLoaiSach.getModel();
        tblModel.setRowCount(0);
        String trangThai = "";
        for (model.LoaiSach ls : list) {
            if (ls.getTrangThai() == 0) {
                trangThai = "Hết";
            } else {
                trangThai = "Còn";
            }
            Object[] row = new Object[]{ls.getMaLoai(), ls.getTenLoai(), trangThai};
            tblModel.addRow(row);
        }
    }

    public void Showdetail(int index) {
        try {
            txtMaloai.setText(tblLoaiSach.getValueAt(index, 0) + "");
            txtTenloai.setText(tblLoaiSach.getValueAt(index, 1) + "");
            Object objtrangthai = (tblLoaiSach.getValueAt(index, 2));
            if (objtrangthai instanceof String) {
                String trangThai = objtrangthai + "";
                if (trangThai.equals("Hết")) {
                    cbotrangThai.setSelectedItem("Hết");
                } else {
                    cbotrangThai.setSelectedItem("Còn");
                }
            } else if (objtrangthai instanceof Integer) {
                String trangThai = (String) objtrangthai;
                cbotrangThai.setSelectedItem(trangThai);
            }
        } catch (Exception ex) {
            System.out.println(ex);
        }
    }

    public void search(String maloai) {
        ArrayList<model.LoaiSach> Lists = new ArrayList<>();
        for (model.LoaiSach ls : list) {
            if (ls.getMaLoai().equalsIgnoreCase(maloai)) {
                Lists.add(ls);
                break;
            }
        }

        tblModel.setRowCount(0);
        for (model.LoaiSach ls : Lists) {
            Object[] row = new Object[]{ls.getMaLoai(), ls.getTenLoai(), ls.getTrangThai()};
            tblModel.addRow(row);
        }
    }

