    private void maloai() {
        try {
            listls = loaiSachDAO.getList();
            cbomaloai.removeAllItems();
            for (model.LoaiSach ls : listls) {
                cbomaloai.addItem(ls.getTenLoai());
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public void ShowDetail(int Index) {
        try {
            txtMaSach.setText(tblSach.getValueAt(Index, 0) + "");
            txtTenSach.setText(tblSach.getValueAt(Index, 1) + "");
            txtGiaThue.setText(tblSach.getValueAt(Index, 2) + "");
            txtSoLuong.setText(tblSach.getValueAt(Index, 3) + "");
            Object objTrangThai = tblSach.getValueAt(Index, 4);
            if (objTrangThai instanceof Integer) {
                int trangthai = (int) objTrangThai;
                if (trangthai == 0) {
                    cboTrangThai.setSelectedItem("Hết sách");
                } else {
                    cboTrangThai.setSelectedItem("Còn sách");
                }
            } else if (objTrangThai instanceof String) {
                String trangthai = (String) objTrangThai;
                cboTrangThai.setSelectedItem(trangthai);
            }
            txtMoTa.setText(tblSach.getValueAt(Index, 5) + "");
            txtTacGia.setText(tblSach.getValueAt(Index, 6) + "");
            txtngayxb.setText(tblSach.getValueAt(Index, 7) + "");
            Object objmaloai = tblSach.getValueAt(Index, 8);
            cbomaloai.setSelectedItem(objmaloai);
        } catch (Exception e) {
            System.out.println(e);
        }
    }

    public void filltotable() {
        tblmoModel.setRowCount(0);
        String trangThai = "";
        for (model.Sach qls : list) {
            if (qls.getTrangthai() == 0) {
                trangThai = "Hết sách";
            } else {
                trangThai = "Còn sách";
            }
            String tenloai = "";
            for (int i = 0; i < listls.size(); i++) {
                if (listls.get(i).getMaLoai().equalsIgnoreCase(qls.getMaloai() + "")) {
                    tenloai = listls.get(i).getTenLoai();
                }
            }
            Object[] row = new Object[]{qls.getMasach(), qls.getTensach(), qls.getGiathue(), qls.getSoluong(), trangThai, qls.getMota(), qls.getTentacgia(), qls.getNgay(), tenloai};
            tblmoModel.addRow(row);
        }
    }

