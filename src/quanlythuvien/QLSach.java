    private void btnTimkiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimkiemActionPerformed
        String maSach = txtTimkiem.getText().trim();

        if (maSach.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập mã sách cần tìm!");
            return;
        }
        try {
            Sach sach = qLySachDAO.search(maSach);
            if (sach != null) {
                list.clear();
                list.add(sach);
                filltotable();
            } else {
                JOptionPane.showMessageDialog(this, "Không tìm thấy sách có mã " + maSach);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Lỗi: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnTimkiemActionPerformed

