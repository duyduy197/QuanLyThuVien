    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int selectedRow = tblSach.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn một chuyên đề để xóa.");
            return;
        }
        String selectedMaSach = tblmoModel.getValueAt(selectedRow, 0).toString();
        int confirm = JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn xóa chuyên đề này không?", "Xác nhận xóa", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            boolean result = qLySachDAO.hide(selectedMaSach);
            if (result) {
                JOptionPane.showMessageDialog(this, "Đã xóa thành công!");
                filltotable();
            } else {
                JOptionPane.showMessageDialog(this, "Lỗi! Không thể xóa.");
            }
        }
        list = qLySachDAO.getlist();
        filltotable();
        txtMaSach.setText("");
        txtTenSach.setText("");
        txtGiaThue.setText("");
        txtSoLuong.setText("");
        cboTrangThai.setSelectedIndex(-1);
        txtMoTa.setText("");
        txtngayxb.setText("");
        cbomaloai.setSelectedIndex(0);

    }//GEN-LAST:event_btnXoaActionPerformed

