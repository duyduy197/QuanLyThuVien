    private void tblThanhVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblThanhVienMouseClicked

        int rowIndex = tblThanhVien.getSelectedRow();
        if (rowIndex >= 0) {
            String maKH = (String) tblThanhVien.getValueAt(rowIndex, 0);
            String emailKH = (String) tblThanhVien.getValueAt(rowIndex, 1);
            String soDT = (String) tblThanhVien.getValueAt(rowIndex, 2);
            String tenKH = (String) tblThanhVien.getValueAt(rowIndex, 3);
            String cccd = (String) tblThanhVien.getValueAt(rowIndex, 4);
            String gioiTinh = (String) tblThanhVien.getValueAt(rowIndex, 5);
            String tuoiKH = String.valueOf(tblThanhVien.getValueAt(rowIndex, 6));
            txtMaKH.setText(maKH);
            txtTenKH.setText(tenKH);
            txtuoi.setText(tuoiKH);
            txtEmailKH.setText(emailKH);
            txtCCCD.setText(cccd);
            txtSoDT.setText(soDT);
            if (gioiTinh.equals("Nam")) {
                rdoNam.setSelected(true);
                rdoNu.setSelected(false);
            } else {
                rdoNam.setSelected(false);
                rdoNu.setSelected(true);
            }
        }
    }//GEN-LAST:event_tblThanhVienMouseClicked

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed

        String maKH = txtMaKH.getText();
        String tenKH = txtTenKH.getText();
        int tuoiKH = Integer.parseInt(txtuoi.getText());
        String emailKH = txtEmailKH.getText();
        String cccd = txtCCCD.getText();
        int gioiTinh = rdoNam.isSelected() ? 1 : 0;
        String soDT = txtSoDT.getText();
        KhachHang khachHang = new KhachHang(maKH, emailKH, tenKH, soDT, cccd, gioiTinh, tuoiKH);
        ListKH.add(khachHang);
        khachHangDAO.add(maKH, emailKH, tenKH, soDT, cccd, gioiTinh, tuoiKH);
        FillToTable();
        clearInputFields();
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        int selectedIndex = tblThanhVien.getSelectedRow();
        if (selectedIndex != -1) {
            KhachHang khachHang = ListKH.get(selectedIndex);
            String tenKH = txtTenKH.getText();
            int tuoiKH = Integer.parseInt(txtuoi.getText());
            String emailKH = txtEmailKH.getText();
            String cccd = txtCCCD.getText();
            int gioiTinh = rdoNam.isSelected() ? 1 : 0;
            String soDT = txtSoDT.getText();
            khachHang.setEmail(emailKH);
            khachHang.setTen(tenKH);
            khachHang.setTuoi(tuoiKH);
            khachHang.setSoCCCD(cccd);
            khachHang.setGioiTinh(gioiTinh);
            khachHang.setSdt(soDT);
            khachHangDAO.edit(emailKH, emailKH, tenKH, soDT, cccd, gioiTinh, tuoiKH);
            FillToTable();

            JOptionPane.showMessageDialog(this, "Cập nhật thông tin khách hàng thành công!");
        } else {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn một khách hàng để sửa!");
        }
    }//GEN-LAST:event_btnSuaActionPerformed

