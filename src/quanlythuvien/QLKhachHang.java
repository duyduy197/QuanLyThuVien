    public void InitTable() {
        tblModel = (DefaultTableModel) tblThanhVien.getModel();
        tblModel.setColumnIdentifiers(new String[]{"MaKH", "Email", "SDT", "Ten", "CCCD", "GioiTinh", "Tuoi"});
    }

    public void FillToTable() {
        tblModel = (DefaultTableModel) tblThanhVien.getModel();
        tblModel.setRowCount(0);
        for (model.KhachHang kh : ListKH) {
            String gioiTinh;
            if (kh.getGioiTinh() == 1) {
                gioiTinh = "Nam";
            } else {
                gioiTinh = "Nữ";
            }
            Object[] row = new Object[]{kh.getMaKH(), kh.getEmail(), kh.getSdt(), kh.getTen(), kh.getSoCCCD(), gioiTinh, kh.getTuoi()};
            tblModel.addRow(row);
        }
    }

