    private void btnAnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAnActionPerformed
        int selectedIndex = tblThanhVien.getSelectedRow();
        if (selectedIndex != -1) {
            KhachHang khachHang = ListKH.get(selectedIndex);
            khachHangDAO.delete(khachHang.getMaKH());
            ListKH.remove(selectedIndex);
            FillToTable();
            JOptionPane.showMessageDialog(this, "Xóa thông tin khách hàng thành công!");
        } else {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn một khách hàng để xóa!");
        }
    }//GEN-LAST:event_btnAnActionPerformed

    private void jLabel9MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel9MouseClicked
        String Makh = txttiemkiem.getText();
        if (Makh.isEmpty()) {
            ListKH = khachHangDAO.getList();
        } else {
            ListKH = khachHangDAO.search(Makh);
        }
        FillToTable();
    }//GEN-LAST:event_jLabel9MouseClicked

